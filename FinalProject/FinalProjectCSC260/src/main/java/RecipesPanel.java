    private void searchRecipeIngredientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchRecipeIngredientActionPerformed
        response = MealAPI.searchMeal(searchBar.getText());
        if (response != null && response.meals != null) {
            resultsList.setListData(response.getNames());
        } else {
            // Handle no results found or error
            resultsList.setListData(new String[0]);
        }
    }//GEN-LAST:event_searchRecipeIngredientActionPerformed

