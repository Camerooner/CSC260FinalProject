    private void displayMeal(MealAPI.MealData data){
        mealImg.setIcon(new ImageIcon(data.loadImage().getScaledInstance(250, 250, BufferedImage.SCALE_SMOOTH)));
        ingredientList.setListData(data.getIngredients());
        String instructionsAndTutorial = data.getInstructions() + " Youtube tutorial: " + data.strYoutube;
        taInstructionsAndTutorial.setText(instructionsAndTutorial);
        labelID.setText("Meal ID: " + data.idMeal);
        labelName.setText("Meal Name: " + data.strMeal);
        labelCategory.setText("Category: " + data.strCategory);
    }
    
